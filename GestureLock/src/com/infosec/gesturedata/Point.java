package com.infosec.gesturedata;

public class Point {

	public float x;
	public float y;
	public float z;
	
	public Point() { }
	public Point(float x, float y, float z) {
		this.x = x;
		this.y = y;
		this.z = z;		
	}
	public void set(float x, float y, float z) {
		this.x = x;
		this.y = y;
		this.z = z;		
	}
}
